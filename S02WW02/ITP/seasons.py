number = int(input("Input Number Between 1 and 4: "))

def seasons():
    if number == 1:
        print("It is winter...brr")

    elif number == 2:
        print('It is Spring')

    elif number == 3:
        print('It is Summer')

    elif number == 4:
        print('It is Autumn')
    
    else:
        print('A number between 1 and 4 please')   

seasons()
    